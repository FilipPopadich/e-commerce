const img = (id) => {
    if (!id) return '';

    if (typeof id === 'string' && id.startsWith('http')) {
        return id;
    }

    const base = id.startsWith('premium_photo-')
        ? 'https://plus.unsplash.com'
        : 'https://images.unsplash.com';

    return `${base}/${id}?w=600&q=60`;
};

